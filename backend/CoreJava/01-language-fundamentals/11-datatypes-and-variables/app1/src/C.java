class C 
{
	public static void main(String[] args) 
	{   
		//local variable
		int i;//declaration
		i = 10;//initialization
		System.out.println(i);
		i = 20;//re-initialization
		System.out.println(i);
		i = 30;//re-initialization
		System.out.println(i);
		System.out.println(i);
	}
}
// the same variable can be reinitialized any number of times.